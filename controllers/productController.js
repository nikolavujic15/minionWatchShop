export const createProductController = async (req, res) => {
  try {
    const { name, description, price, kategorije, quantity } =
      req.fields;
    const { photo } = req.files;
    //validation
    switch (true) {
      case !name:
        return res.status(500).send({ error: "Ime je obavezno" });
      case !description:
        return res.status(500).send({ error: "Opis je obavezan" });
      case !price:
        return res.status(500).send({ error: "Cena je obavezna" });
      case !kategorije:
        return res.status(500).send({ error: "Kategorija je obavezna" });
      case !quantity:
        return res.status(500).send({ error: "Kolicina je obavezna" });
      case photo && photo.size > 1000000:
        return res
          .status(500)
          .send({ error: "Fotografija je obavezna i mora biti manja od 1mb" });
    }

    const products = new productModel({ ...req.fields, slug: slugify(name) });
    if (photo) {
      products.photo.data = fs.readFileSync(photo.path);
      products.photo.contentType = photo.type;
    }
    await products.save();
    res.status(201).send({
      success: true,
      message: "Proizvod sacuvan",
      products,
    });
  } catch (error) {
    console.log(error);
    res.status(500).send({
      success: false,
      error,
      message: "Error in crearing product",
    });
  }
};

